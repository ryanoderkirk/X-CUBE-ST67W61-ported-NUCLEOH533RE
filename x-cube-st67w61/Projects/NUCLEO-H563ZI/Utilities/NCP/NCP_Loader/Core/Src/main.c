static void uart_pass_through(void)
{
  pushbutton = 0;

  while (1)
  {
    /* Bit copy */
    /* USART_RX => STLINK_TX */
    if ((UART_RX_GPIO_Port->IDR) & ((uint32_t)UART_RX_Pin))
    {
      STLINK_TX_GPIO_Port->BSRR = (uint32_t)STLINK_TX_Pin; /* set */
    }
    else
    {
      STLINK_TX_GPIO_Port->BSRR = (uint32_t)(STLINK_TX_Pin << 16); /* reset */
    }
    /* STLINK_RX => USART_TX */
    if ((STLINK_RX_GPIO_Port->IDR) & ((uint32_t)STLINK_RX_Pin))
    {
      UART_TX_GPIO_Port->BSRR = (uint32_t)UART_TX_Pin; /* set */
    }
    else
    {
      UART_TX_GPIO_Port->BSRR = (uint32_t)(UART_TX_Pin << 16); /* reset */
    }
    if (pushbutton)
    {
      break;
    }
  }
}

static void uart_bypass(uint32_t flash)
{

  LL_GPIO_ResetOutputPin(LED_RED_GPIO_Port, LED_RED_Pin);
  LL_GPIO_ResetOutputPin(LED_GREEN_GPIO_Port, LED_GREEN_Pin);

  for (int32_t i = 0 ; i < 3; i++)
  {
    if (flash)
    {
      LL_GPIO_ResetOutputPin(LED_RED_GPIO_Port, LED_RED_Pin);
    }
    else
    {
      LL_GPIO_ResetOutputPin(LED_GREEN_GPIO_Port, LED_GREEN_Pin);
    }
    HAL_Delay(100);
    if (flash)
    {
      LL_GPIO_SetOutputPin(LED_RED_GPIO_Port, LED_RED_Pin);
    }
    else
    {
      LL_GPIO_SetOutputPin(LED_GREEN_GPIO_Port, LED_GREEN_Pin);
    }
    HAL_Delay(100);
  }

  if (flash)
  {
    LL_GPIO_SetOutputPin(BOOT_GPIO_Port, BOOT_Pin);
  }
  else
  {
    LL_GPIO_ResetOutputPin(BOOT_GPIO_Port, BOOT_Pin);
  }

  /* RESET NCP CHIP */
  LL_GPIO_ResetOutputPin(CHIP_EN_GPIO_Port, CHIP_EN_Pin);
  HAL_Delay(100);
  LL_GPIO_SetOutputPin(CHIP_EN_GPIO_Port, CHIP_EN_Pin);

  if (flash)
  {
    /* CLEAR BOOT */
    HAL_Delay(100);
    LL_GPIO_ResetOutputPin(BOOT_GPIO_Port, BOOT_Pin);
  }
  uart_pass_through();
}

